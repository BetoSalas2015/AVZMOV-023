 var a = 5.5;           // Number
 console.log(a);

 a = "Hola"             //  String
 console.log(a);
 
 a = 'a'             // caracteres
 console.log(a);
 
 a = function() {}             // funciones
 console.log(a);
 
 a = undefined;     //  Indefinido
 console.log(a);
 
 a = null           // nada
 console.log(a);
 
 a = { }         //  Objetos
 console.log(a);

 
